# type casting is a process of converting one data type to the other

# int() -> converts to int

# float() -> converts to float.

# str() -> converts to string.

# bool() -> converts to boolean


# converts string to integer

numStr = "10";

numInt = int(numStr);
print(type(numInt));


# integer to string

num = 24;

numStr = str(num);

print(type(numStr));

# float to integer

pi = 3.14;

num1 = int(pi);
print(num1);


